CREATE TABLE TB005_EMPRESA(
    CO_EMPRESA SERIAL PRIMARY KEY,
    CO_CNPJ VARCHAR(14) NOT NULL,
    NO_RAZAO_SOCIAL VARCHAR(100) NOT NULL,
    NO_NOME_FANTASIA VARCHAR(100) NOT NULL,
    IC_SITUACAO CHAR(1) NOT NULL,
    NO_CLASSIFICACAO_TRIBUTARIA VARCHAR(150) NOT NULL,
    NO_NATUREZA_JURIDICA VARCHAR(150),
    IC_REGISTRO_ELETRONICO_EMPREGADO CHAR(1) NOT NULL,
    IC_COOPERATIVA CHAR(1) NOT NULL,
    IC_CONSTRUTORA CHAR(1) NOT NULL,
    IC_DESONERACAO_FOLHA CHAR(1) NOT NULL,
    IC_ENTIDADE_EDUCATIVA CHAR(1) NOT NULL,
    IC_EMPRESA_TRABALHO_TEMPORARIO CHAR(1) NOT NULL,
    IC_SITUACAO_PESSOA_JURIDICA CHAR(1) NOT NULL,
    DT_ABERTURA DATE,
    NO_TIPO_EMPRESA VARCHAR(10),
    CONTATO_CO INT,
    ENDERECO_CO INT,
    FOREIGN KEY (CONTATO_CO) REFERENCES TB003_CONTATO (CO_CONTATO),
    FOREIGN KEY (ENDERECO_CO) REFERENCES TB002_ENDERECO (CO_ENDERECO)
);